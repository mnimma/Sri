
public class Operators2 {

	public static void main(String args[])

	{
		int a, b, c;

		a = 10;
		b = 20;

		System.out.println("a+b=" + (a + b));
		System.out.println("a-b=" + (a - b));
		System.out.println("a*b=" + (a * b));
		System.out.println("a/b=" + (a / b));

		a++;
		System.out.println("a value after a++:" + a);
		a--;
		System.out.println("a value after a--:" + a);
	}
}
