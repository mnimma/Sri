
public class ElseIf2 {

	public static void main(String args[]) {
		int x = 32;

		if (x < 10) {
			System.out.println("x is not less than 10  ");
		} else if (x < 20) {
			System.out.println("x is not less than 20  ");
		}

		else if (x < 35) {
			System.out.println("x is less than 35");
		}

		else
			System.out.println(" This is Else Statement");
	}
}
